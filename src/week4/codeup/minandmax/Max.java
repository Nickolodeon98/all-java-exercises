package week4.codeup.minandmax;

public class Max {
    private int maxNum;
    private String maxStr;

    public Max(int maxNum, String maxStr) {
        this.maxNum = maxNum;
        this.maxStr = maxStr;
    }

    public int getMaxNum() {
        return maxNum;
    }

    public String getMaxStr() {
        return maxStr;
    }
}
