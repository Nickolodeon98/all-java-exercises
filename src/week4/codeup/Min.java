package week4.codeup;

public class Min {
    private int minNum;
    private String minStr;

    public Min(int minNum, String minStr) {
        this.minNum = minNum;
        this.minStr = minStr;
    }

    public int getMinNum() {
        return minNum;
    }

    public String getMinStr() {
        return minStr;
    }
}
